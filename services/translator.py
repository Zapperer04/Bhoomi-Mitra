def normalize_city_input(text: str):
    if not text:
        return None
    text = text.strip().lower()
    if text in CITY_LABELS:
        return text
    for canonical, labels in CITY_LABELS.items():
        for lbl in labels.values():
            if text == lbl.lower():
                return canonical
    return None


